//Criar um programa com uma condicional composta para verificar a temperatura entre "frio", "afradavel" ou "quente"
let temperatura = 30
if (temperatura < 15){
    console.log("Está frio")
} else if (temperatura >= 15 && temperatura <= 25){
    console.log("Está afradavel")
} else {
    console.log("Está quente")
}